package Questao1;

public class Questao1 {

	public static void main(String[] args) {
		////Classe - é uma estrutura onde vão ser criados objetos além de atribuir valores e propriedades pra esses objetos
		////Objeto - é uma entidade asbtrata ou não que possui funções e atributos
		////Metodo e Atributo - Atributo são caracteristicas e valores do objeto, ja o metodo são as funções que o objeto pode realizar
		////Encapsulamento - É uma forma de se ocultar dados e metodos, de tal forma que so se pode acessar tal atributo ou metodo de uma maneira especifica
		////Visibilidade de atributos e métodos - Public: é acessivel em qualquer classe e independente do pacote que se encontra; Private: é acessivel apenas na proria classe; Protected: acessivel apenas no mesmo pacote
		////Herança - Metodos e atributos que são passados de uma classe pai para uma classe filho
		////Polimorfismo - Fenomeno que ocorre quando duas ou mais classes descendentes de uma mesma classe realizam um comportamento diferente para um metodo em comum
		////Classes abstratas - Classe que apenas pode ser ancestral e nunca pode gerar instancias
		////Construtores - É um metodo que tem como unica função definir valores assim que é instanciada
		////Get e Set - São metodos que trabalham com o encapsulamento e tem como função acessar atributos e metodos pouco acessiveis
		////Sobrecarga de métodos - Ocorre quando se tem varios metodos com o mesmo nome mas con funções diferentes
		////Sobrescrita de métodos - Ocorre quando uma classe filho da uma nova função à um metodo herdado pelo pai
		////Palavras reservadas - Super: chamar um metodo da classe pai que foi sobrescrita; This: utilizado ao se referir à um atributo pertecente a propria classe; Final: se refere à uma classe que não pode gerar filhos
		////Relação de objetos - Ter: Quando um objeto pode ter outro objeto entre seus atributos; Usar: é quando alem de apenas ter outro objeto ainda o utiliza para realizar funções; Ser: representa a herança de um objeto ao derivar de uma classe ancestral
	}

}
